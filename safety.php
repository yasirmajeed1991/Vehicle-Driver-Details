</span></span></span><![endif]><span lang=EN style='mso-ansi-language:EN'>Lox
Lift&nbsp;has a no-sex rule. Sexual contact is prohibited while using
the&nbsp;Lox Lift&nbsp;apps, including during a trip or on a delivery.</span></p>

