lang=EN style='mso-ansi-language:EN'>Behaviors and comments that could make
people feel uncomfortable are not acceptable. Examples include nudges,
whistles, and winks. Don’t touch or flirt with people you
don’t&nbsp;know.&nbsp;<o:p></o:p></span></p>

<p class=MsoNoSpacing style='margin-left:36.0pt;text-indent:-18.0pt;mso-list:
l1 level1 lfo2'><![if !supportLists]><span style='mso-fareast-font-family:Calibri'><span
style='mso-list:Ignore'>2.<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span></span><![endif]><span lang=EN style='mso-ansi-language:EN'>Certain
conversations that could be perceived as harmless can be offensive. Don’t
comment on appearance, perceived gender identity, or sexual orientation.
Refrain from asking unrelated personal questions, such as, “Are you in a
relationship?” Avoid discussing your own or someone else’s sex life, using
explicit language, or making jokes about&nbsp;sex.</span></p>

<p class=MsoNoSpacing style='margin-left:36.0pt;text-indent:-18.0pt;mso-list:
