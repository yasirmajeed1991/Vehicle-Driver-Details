<p class=MsoNormal style='text-align:justify;text-autospace:none'><b><span
lang=EN style='mso-ansi-language:EN'>Safety and Respect for all</span></b></p>

<p class=MsoNormal style='margin-right:195.85pt;text-align:justify;text-autospace:
none'><span lang=EN style='mso-ansi-language:EN'>Lox Lift's Community
Guidelines &nbsp;&nbsp;</span></p>

<p class=MsoNormal style='text-align:justify;text-autospace:none'><span
lang=EN style='mso-ansi-language:EN'>Our guidelines were developed to help make
every experience feel safe, respectful, and&nbsp;<span class=SpellE>positive.Everyone</span>
<span class=GramE>who</span> signs up for a Lox Lift account, including
drivers, riders and business owners, is agreeing to and is expected to follow
the&nbsp;guidelines.&nbsp; Any Lox Lift user that is found to have falsified
ANY of their profile information and/or engaged in ANY type of criminal
activity whilst in the commission of a Lox Lift ride, will not only be banned from
our platform, but will also be prosecuted to the fullest extent of the
law.&nbsp; Lox Lift will work actively with in concert with not only TNM <span
class=SpellE>Mpamba</span> and <span class=SpellE>Airtel</span>, but also with
Malawian police enforcement and homeland security to track down and apprehend
the alleged criminal party or parties.&nbsp; By registering with Lox Lift you
are agreeing to allow Lox Lift to share your personal and/or private profile
information with government law enforcement agencies in the event of alleged
fraud, theft, assault or any other form of criminality.</span></p>

<p class=MsoNormal style='text-align:justify;text-autospace:none'><span
lang=EN style='mso-ansi-language:EN'>Treat everyone with respect we believe
that everyone should feel supported and welcomed. That’s why we’ve created
standards on physical contact, sexual assault and misconduct, threatening and
rude behavior, unwanted contact, and&nbsp;discrimination.</span></p>

<p class=MsoNormal style='text-align:justify;text-autospace:none'><b><span
lang=EN style='mso-ansi-language:EN'>Physical contact:&nbsp; </span></b></p>

<p class=MsoNormal style='text-align:justify;text-autospace:none'>Don’t touch
strangers or anyone you just met while using any of&nbsp;Lox Lift’s apps.
Hitting, hurting, or otherwise intending to hurt anyone is never&nbsp;allowed.</p>

<p class=MsoNormal style='text-align:justify;text-autospace:none'><b><span
lang=EN style='mso-ansi-language:EN'>Sexual assault and sexual
misconduct:&nbsp; </span></b></p>

<p class=MsoNormal style='text-align:justify;text-autospace:none'><span
lang=EN style='mso-ansi-language:EN'>Sexual assault and sexual misconduct of
any kind is prohibited. Sexual assault and misconduct refers to sexual contact
or behavior without explicit consent of the other&nbsp;person. Personal space
and privacy should be respected. The following list provides examples of
inappropriate&nbsp;conduct.&nbsp;</span></p>

<p class=MsoNoSpacing style='margin-left:36.0pt;text-indent:-18.0pt;mso-list:
l1 level1 lfo2'><![if !supportLists]><span lang=EN style='mso-fareast-font-family:
